SELECT NOMBRE, LOCALIDAD
FROM ALUM
MINUS
SELECT NOMBRE, LOCALIDAD
FROM ANTIGUOS
ORDER BY LOCALIDAD;

SELECT NOMBRE, LOCALIDAD
FROM ALUM
WHERE (NOMBRE, LOCALIDAD) NOT IN(
    SELECT NOMBRE, LOCALIDAD
    FROM ANTIGUOS)
ORDER BY LOCALIDAD;
