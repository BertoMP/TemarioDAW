SELECT NOMBRE
FROM ANTIGUOS
WHERE NOMBRE IN (
    SELECT NOMBRE 
    FROM ALUM);

SELECT AN.NOMBRE
FROM ALUM A
INNER JOIN ANTIGUOS AN ON A.NOMBRE = AN. NOMBRE;

