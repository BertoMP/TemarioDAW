--CREAR TABLA PROPIETARIO
CREATE TABLE propietario
(
    dni VARCHAR2(9) PRIMARY KEY
);

--CREAR TABLA PISO
CREATE TABLE piso
(
    ref VARCHAR2(5) PRIMARY KEY,
    dni_propietario VARCHAR2(9) REFERENCES propietario (dni),
    fecha_compra DATE NOT NULL
);

