package tema09_Interfaces.ejerciciosResueltos.ejercicioResuelto03_interfazPila.interfaces;

/***********************************************************************************************************************
 * Define la interfaz Pila para números enteros.                                                                       *
 ***********************************************************************************************************************/
public interface Pila {
    /*******************************************************************************************************************
     * Método abstracto encargado de apilar elementos.                                                                 *
     *                                                                                                                 *
     * @param intNum Números a apilar.                                                                                 *
     *******************************************************************************************************************/
    void push(int intNum);

    /*******************************************************************************************************************
     * Método abstracto encargado de popPila elementos.                                                              *
     *                                                                                                                 *
     * @return El método devolverá el Integer desapilado.                                                              *
     *******************************************************************************************************************/
    Integer popPila();
}
