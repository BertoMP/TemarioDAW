package tema12_Colecciones.ejerciciosResueltos.ejercicioResuelto03_InterfazPila.interfaces;

/***********************************************************************************************************************
 * Intefaz pila.                                                                                                       *
 *                                                                                                                     *
 * @param <T> Tipo genérico.                                                                                           *
 ***********************************************************************************************************************/

public interface Pila<T> {
    /*******************************************************************************************************************
     * Método para apilar objetos.                                                                                     *
     *                                                                                                                 *
     * @param nuevo Objeto a apilar.                                                                                   *
     *******************************************************************************************************************/
    void push(T nuevo);

    /*******************************************************************************************************************
     * Método para desapilar objetos.                                                                                  *
     *                                                                                                                 *
     * @return Devuelve el objeto T desapilado.                                                                        *
     *******************************************************************************************************************/
    T pop();
}
